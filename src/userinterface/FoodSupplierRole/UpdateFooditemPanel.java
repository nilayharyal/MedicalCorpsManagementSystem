    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        Iterator it = organization.getFoodSuppliesInventory().getFoodInventory().entrySet().iterator();
        while (it.hasNext()) {
            Map.Entry pair = (Map.Entry)it.next();
            if(pair.getKey().equals(name)){
                if(!name.equals(nameTextField.getText())){
                    Object obj = organization.getFoodSuppliesInventory().getFoodInventory().remove(name);
                    organization.getFoodSuppliesInventory().getFoodInventory().put(nameTextField.getText(), Integer.parseInt(quantityTextField.getText()));
                    JOptionPane.showMessageDialog(null, "Food Item Updated!");
                    userProcessContainer.remove(this);
                    Component[] componentArray = userProcessContainer.getComponents();
                    Component component = componentArray[componentArray.length - 1];
                    ManageFoodSuppliesJPanel dwjp = (ManageFoodSuppliesJPanel) component;
                    dwjp.populateTable();
                    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                    layout.previous(userProcessContainer);
                }
                else{
                    organization.getFoodSuppliesInventory().getFoodInventory().put(name, Integer.parseInt(quantityTextField.getText()));
                    JOptionPane.showMessageDialog(null, "Food Item Updated!");
                    userProcessContainer.remove(this);
                    Component[] componentArray = userProcessContainer.getComponents();
                    Component component = componentArray[componentArray.length - 1];
                    ManageFoodSuppliesJPanel dwjp = (ManageFoodSuppliesJPanel) component;
                    dwjp.populateTable();
                    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                    layout.previous(userProcessContainer);
                }                
            }
        }
    }//GEN-LAST:event_saveButtonActionPerformed

