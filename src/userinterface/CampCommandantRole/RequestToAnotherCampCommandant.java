    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed
        try {
            String message = descriptionTextArea.getText();
            String requestCat = requestComboBox.getSelectedItem().toString();
            NewRequestFromCampCommandantToCampCommandant request = new NewRequestFromCampCommandantToCampCommandant();
            request.setMessage(message);
            request.setRequestCategory(requestCat);
            request.setSender(userAccount);
            request.setStatus("Sent");

//            Organization org = null;
            ArrayList<Organization> orgList = new ArrayList<Organization>();
            for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (organization instanceof MilitaryCampOrganization) {
//                    org = organization;
                    orgList.add(organization);
                }
            }
            for (Organization org : orgList) {
                if (org != null) {
                    org.getRequestQueueCampCommandantCampCommandant().getworkRequestCampCommandantCampCommandant().add(request);
                }
            }
            userAccount.getRequestQueueCampCommandantCampCommandant().getworkRequestCampCommandantCampCommandant().add(request);
            String recepient = "";
            for (UserAccount ua : enterprise.getSoldierUADirectory().getUserAccountList()) {
                if (ua.getEmail() != null) {
                    recepient = recepient + ua.getEmail() + ",";
                }
            }
            if (!recepient.isEmpty()) {
                send.mail(recepient, requestCat, message);
            }
            JOptionPane.showMessageDialog(null, "Request succesfully sent to doctors");
            descriptionTextArea.setText("");
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(null, "Null Pointer Exception Occurred!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Some Exception Occurred!");
        }
    }//GEN-LAST:event_requestTestJButtonActionPerformed

