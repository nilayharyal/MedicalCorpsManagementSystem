    private void populateCategoryComboBox() {
        requestComboBox.removeAllItems();
        requestComboBox.addItem(Soldiers.RequestCategory.SendingPatients.getValue());
    }

    private void populatePatientNameComboBox() {
        patientNameComboBox.removeAllItems();

        for (Soldiers s : organization.getSoldierDirectory().getSoldierList()) {
            if (s.getRoleType().equals(Role.RoleType.BaseSoldier.getValue())) {
                patientNameComboBox.addItem(s);
            }
        }
    }

    private void saveAsPDFButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveAsPDFButtonActionPerformed
        Document newDocument = new Document();
        String myFilepath = userAccount.getUsername() + "_RequestToAnotherCampCommandant_" + pdfCount + ".pdf";
        try {
            PdfWriter.getInstance(newDocument, new FileOutputStream(myFilepath));
        } catch (DocumentException | FileNotFoundException ex) {
            Logger.getLogger(RequestToAnotherCampCommandant.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            newDocument.open();
            newDocument.add(new Paragraph("Hi,"));
            newDocument.add(new Paragraph(userAccount.getUsername().toUpperCase()));
            newDocument.add(new Paragraph(" "));
            newDocument.add(new Paragraph("Your Request has been send to another Camp Commandant."));
            newDocument.add(new Paragraph(" "));
            newDocument.add(new Paragraph("Request Category - " + requestComboBox.getSelectedItem()));
            newDocument.add(new Paragraph("Patient Name - " + patientNameComboBox.getSelectedItem()));
            newDocument.add(new Paragraph("Request Description - " + descriptionTextArea.getText()));
            newDocument.add(new Paragraph(" "));
            newDocument.add(new Paragraph("Thanks."));
            newDocument.close();
            JOptionPane.showMessageDialog(null, " Report Generated where the project is placed!");
            if (Desktop.isDesktopSupported()) {
                try {
                    File myFile = new File(myFilepath);
                    Desktop.getDesktop().open(myFile);
                } catch (IOException ex) {

                }
            }

        } catch (DocumentException ex) {
            Logger.getLogger(RequestToAnotherCampCommandant.class.getName()).log(Level.SEVERE, null, ex);
        }
        pdfCount += 1;
    }//GEN-LAST:event_saveAsPDFButtonActionPerformed

