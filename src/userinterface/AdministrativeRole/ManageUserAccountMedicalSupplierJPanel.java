    private boolean passwordpatterncorrect(String name)
    {
        Pattern pUser= Pattern.compile( "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@" + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        Matcher mUser= pUser.matcher(name);
        if(mUser.find()){ return true; }
        else { return false;}
    }
    
