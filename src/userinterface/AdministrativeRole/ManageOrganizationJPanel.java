    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJButtonActionPerformed
        // TODO add your handling code here:
        try {
            int selectedRow = organizationJTable.getSelectedRow();
            if (selectedRow < 0) {
                JOptionPane.showMessageDialog(null, "Please select a row.");
            } else {
                int selectionButton = JOptionPane.YES_NO_OPTION;
                int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??", "Warning", selectionButton);
                if (selectionResult == JOptionPane.YES_OPTION) {
                    Organization org = (Organization) organizationJTable.getValueAt(selectedRow, 0);
                    for (Organization s1 : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (s1 == org) {
                            enterprise.getOrganizationDirectory().removeOrganization(s1);
                            JOptionPane.showMessageDialog(null, org.getName() + " removed from the enterprise!");
                            break;
                        }
                    }
                    longitudeTxt.setText("");
                    populateTable();
                }
            }
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(null, "Null Pointer Exception Occurred!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Some Exception Occurred!");
        }
    }//GEN-LAST:event_deleteJButtonActionPerformed

