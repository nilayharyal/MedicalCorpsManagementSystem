    private void populateCombo(Enterprise.EnterpriseType enterpriseType) {
        organizationJComboBox.removeAllItems();
        switch (enterpriseType) {
            case ArmedForces:
                organizationJComboBox.addItem(Organization.Type.Soldiers);
                organizationJComboBox.addItem(Organization.Type.Doctors);
                break;
            case MedicalSuppliers:
                organizationJComboBox.addItem(Organization.Type.MedicalSuppliers);
                break;
            case FoodSuppliers:
                organizationJComboBox.addItem(Organization.Type.FoodSuppliers);
                break;
            default:
                break;
        }
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();

        model.setRowCount(0);

        directory.getOrganizationList().stream().map((organization) -> {
            Object[] row = new Object[2];
            row[0] = organization;
            return row;
        }).forEachOrdered((row) -> {
            model.addRow(row);
        });
    }

